import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const defaultSourceWikiRoot = path.resolve(root, '..', '..', '..', 'agents-wiki', 'wiki');
const sourceInput = path.resolve(
  process.env.VICODE_GUIDANCE_WIKI_SOURCE ??
    process.env.VICODE_GUIDANCE_SOURCE ??
    defaultSourceWikiRoot
);
const outputRoot = path.join(root, 'resources', 'vicode-guidance');
const privateName = process.env.VICODE_GUIDANCE_PRIVATE_NAME ?? ['Ky', 'le'].join('');
const privateSlug = process.env.VICODE_GUIDANCE_PRIVATE_SLUG ?? `${privateName.toLowerCase()}-`;

const wikiFiles = [
  'Agent Runtime Patterns.md',
  'Capability Routing Standard.md',
  'Cleanup And Stale Artifact Discipline.md',
  'Code Organization Standard.md',
  'Context Packet Standard.md',
  'Design Taste Translation.md',
  'Docs As Code.md',
  'Execution Discipline.md',
  'Frontend Quality Gate.md',
  'Frontend Standards.md',
  'Markdown KB Retrieval Design.md',
  'Prompt To Execution Packet.md',
  'Reference To Code Workflow.md',
  'Search And Retrieval.md',
  'Security And Secrets.md',
  'Source-Backed Workflow.md',
  'Task Routing.md',
  'UI Review Rubric.md',
  'UX Writing Standards.md',
  'Verification Standards.md',
  'Writing Anti-Patterns.md'
];

function resolveSourceWikiRoot(sourcePath) {
  if (existsSync(path.join(sourcePath, 'Task Routing.md'))) {
    return sourcePath;
  }

  const nestedWikiPath = path.join(sourcePath, 'wiki');
  if (existsSync(path.join(nestedWikiPath, 'Task Routing.md'))) {
    return nestedWikiPath;
  }

  return sourcePath;
}

const sourceWikiRoot = resolveSourceWikiRoot(sourceInput);

function normalizeNewlines(value) {
  return value.replace(/\r\n/gu, '\n');
}

function escapeRegex(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/gu, '\\$&');
}

function sanitizeContent(content) {
  const privateNamePattern = new RegExp(`\\b${escapeRegex(privateName)}\\b`, 'gu');
  const privatePossessivePattern = new RegExp(`\\b${escapeRegex(privateName)}'s\\b`, 'gu');
  const privateSlugPattern = new RegExp(`\\b${escapeRegex(privateSlug)}\\b`, 'giu');
  const privatePreferencesLinkPattern = new RegExp(
    `\\[\\[${escapeRegex(privateName)} Working Preferences\\]\\]`,
    'giu'
  );

  return normalizeNewlines(content)
    .replace(privatePreferencesLinkPattern, 'user style preferences')
    .replace(/\[\[(?:the user|local-user|Working Preferences)\s*Working Preferences\]\]/giu, 'user style preferences')
    .replace(/\[\[Working Preferences\]\]/giu, 'user style preferences')
    .replace(/\[\[Source - D Projects Pattern Sweep\]\]/giu, 'project pattern sweep')
    .replace(privatePossessivePattern, "the user's")
    .replace(privateNamePattern, 'the user')
    .replace(privateSlugPattern, 'local-user')
    .replace(/C:\\Users\\[^\\\s)>\]]+/giu, '<local-user-profile>')
    .replace(/C:\/Users\/[^\/\s)>\]]+/giu, '<local-user-profile>')
    .replace(/\/Users\/[^\/\s)>\]]+/giu, '<local-user-profile>')
    .replace(/D:\\Projects(?:\\[^)\]\n\r>]*)?/giu, '<local-project-root>')
    .replace(/D:\/Projects(?:\/[^)\]\n\r>]*)?/giu, '<local-project-root>')
    .replace(/\\\?\\D:\\Projects(?:\\[^)\]\n\r>]*)?/giu, '<local-project-root>')
    .trim();
}

function readSanitized(relativePath) {
  const sourcePath = path.join(sourceWikiRoot, relativePath);
  if (!existsSync(sourcePath)) {
    throw new Error(`Missing source guidance file: ${sourcePath}`);
  }
  return sanitizeContent(readFileSync(sourcePath, 'utf8'));
}

function ensureParent(targetPath) {
  mkdirSync(path.dirname(targetPath), { recursive: true });
}

function writeGuidanceFile(relativePath, content) {
  const targetPath = path.join(outputRoot, relativePath);
  ensureParent(targetPath);
  writeFileSync(
    targetPath,
    [
      '<!-- Generated by scripts/build-vicode-guidance.mjs from a curated guidance allowlist. -->',
      '<!-- Do not add private local paths, personal identifiers, credentials, or scratch notes here. -->',
      '',
      content.trim(),
      ''
    ].join('\n')
  );
}

function extractFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---\n?/u);
  return match?.[1] ?? '';
}

function extractAliases(frontmatter) {
  const aliases = [];
  const lines = frontmatter.split('\n');

  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index];
    const inlineAliases = line.match(/^aliases:\s*\[(.+)\]\s*$/u);
    if (inlineAliases) {
      aliases.push(
        ...inlineAliases[1]
          .split(',')
          .map((alias) => alias.trim().replace(/^["']|["']$/gu, ''))
      );
      continue;
    }

    if (!/^aliases:\s*$/u.test(line)) {
      continue;
    }

    for (let aliasIndex = index + 1; aliasIndex < lines.length; aliasIndex += 1) {
      const aliasLine = lines[aliasIndex];
      if (!/^\s+-\s+/u.test(aliasLine)) {
        break;
      }
      aliases.push(aliasLine.replace(/^\s+-\s+/u, '').trim().replace(/^["']|["']$/gu, ''));
    }
  }

  return [...new Set(aliases.filter(Boolean))];
}

function extractTitle(fileName, content) {
  const heading = content.match(/^#\s+(.+)$/mu)?.[1]?.trim();
  return heading || fileName.replace(/\.md$/iu, '');
}

function normalizeRoutePath(value) {
  return value.replace(/\\/gu, '/');
}

function buildPageManifest(fileName, content) {
  const title = extractTitle(fileName, content);
  const aliases = extractAliases(extractFrontmatter(content));

  return {
    title,
    path: normalizeRoutePath(path.join('wiki', fileName)),
    obsidianRoute: `[[${title}]]`,
    aliases,
    aliasRoutes: aliases.map((alias) => `[[${alias}]]`)
  };
}

function buildVicodeMd() {
  return `# Vicode Guidance

## Purpose

This file is Vicode's provider-neutral operating guide for AI coding runs.

Use it to choose the smallest useful guidance from the included Vicode wiki. Do not read or load the whole wiki by default.

## Wiki Layout

The packaged guidance is a curated Obsidian-style markdown vault:

- \`VICODE.md\` is the entrypoint and router.
- \`wiki/\` mirrors the selected pages from the source wiki vault.
- Prefer Obsidian routes such as \`[[Task Routing]]\` when available.
- Fall back to packaged markdown paths such as \`wiki/Task Routing.md\` when Obsidian is not available.
- Use \`manifest.json\` to map page titles, aliases, Obsidian routes, and packaged file paths.

## Source Order

1. Current user request
2. App-enforced Vicode safety and trust boundaries
3. Active workspace instructions
4. User personalization
5. Vicode guidance wiki
6. Provider-specific behavior and official docs
7. Clearly labeled inference

## Start Here

Use \`[[Task Routing]]\` first. Choose one lead page for the task family, then add only the support pages that apply.

Use \`[[Source-Backed Workflow]]\` before substantial implementation, debugging, refactoring, or source-sensitive work.

## Using Disclosure

When a run uses Vicode wiki pages, skills, external references, or app/tool capabilities, start the response with one short line:

\`Using: <references>\`

Name only the references actually used. Include the same important references in the final summary when they shaped the work.

## Default Rules

- Keep changes small, source-backed, and verifiable.
- Prefer modular frontend and backend implementation.
- Do not invent architecture when repo-local docs or code already define it.
- Keep security, secrets, and trust boundaries explicit.
- Run focused verification before reporting work as complete.
- Keep final summaries compact: what changed, verification, and concrete next steps only when they exist.
`;
}

function main() {
  if (!existsSync(sourceWikiRoot)) {
    throw new Error(
      `Vicode guidance source wiki not found: ${sourceWikiRoot}. Set VICODE_GUIDANCE_WIKI_SOURCE to the source wiki folder.`
    );
  }

  rmSync(outputRoot, { recursive: true, force: true });
  mkdirSync(path.join(outputRoot, 'wiki'), { recursive: true });

  writeGuidanceFile('VICODE.md', buildVicodeMd());

  const pages = [];
  for (const fileName of wikiFiles) {
    const content = readSanitized(fileName);
    writeGuidanceFile(path.join('wiki', fileName), content);
    pages.push(buildPageManifest(fileName, content));
  }

  writeFileSync(
    path.join(outputRoot, 'manifest.json'),
    `${JSON.stringify(
      {
        schemaVersion: 1,
        generatedAt: new Date().toISOString(),
        source: '<curated-local-guidance-source>/wiki',
        entrypoint: 'VICODE.md',
        vaultRoot: 'wiki',
        routing: {
          kind: 'obsidian-wikilinks',
          preferred: 'obsidianRoute',
          fallback: 'path',
          note: 'Use [[Page Title]] routes when Obsidian is available; use packaged markdown paths otherwise.'
        },
        files: ['VICODE.md', ...pages.map((page) => page.path)],
        pages
      },
      null,
      2
    )}\n`
  );

  console.log(
    JSON.stringify(
      {
        sourceWikiRoot: '<curated-local-guidance-source>/wiki',
        outputRoot: path.relative(root, outputRoot).replace(/\\/gu, '/'),
        copiedWikiFiles: pages.length,
        routing: 'obsidian-wikilinks'
      },
      null,
      2
    )
  );
}

main();
